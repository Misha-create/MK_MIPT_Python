    brushColor(color)
    penColor(color)
    rectangle(x, y, x+250, y+30)
    rectangle(x+100,y,x+220,y-25)
    brushColor("white")
    penColor("white")
    rectangle(x+120,y-5,x+150,y-20)
    rectangle(x+170,y-5,x+200,y-20)
    brushColor("orange")
    penColor("yellow")
    circle(x+60, y+30, 15)
    circle(x+200, y+30, 15)
    brushColor("black")
    penColor("black")
    rectangle(x+250,y+20,x+259,y+23)
